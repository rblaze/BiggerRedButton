/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_USB_PCD_Init();
  MX_USBX_Device_Init();
  /* USER CODE BEGIN 2 */

  const uint32_t timer_arr = __HAL_TIM_GET_AUTORELOAD(&htim2);

  // Default brightness is 2%
  const uint32_t inactive_brightness = timer_arr / 50;
  // Blink from 10% to 100%
  const uint32_t blink_low_brightness = timer_arr / 10;
  const uint32_t blink_high_brightness = timer_arr;

  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, inactive_brightness);

  if (HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4) != HAL_OK) {
    Error_Handler();
  }

  UX_SLAVE_DEVICE *device;
  device = &_ux_system_slave->ux_system_slave_device;

  UX_SLAVE_CLASS_HID_EVENT hid_event;
  ux_utility_memory_set(&hid_event, 0, sizeof(UX_SLAVE_CLASS_HID_EVENT));

  // Report size is 8 bytes: modifier, reserved, and 6 keycode bytes.
  hid_event.ux_device_class_hid_event_length = 8;
  const size_t keycode_offset = 2;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    HAL_PWR_EnterSLEEPMode(PWR_MAINREGULATOR_ON, PWR_SLEEPENTRY_WFI);

    // Some event occured.
    // Button interrupt handler will set the flag.
    if (button_pressed) {
      button_pressed = 0;

      int num_blinks = 3;
      uint32_t blink_time_ms = 500;

      if ((device->ux_slave_device_state == UX_DEVICE_CONFIGURED) && (keyboard_instance != UX_NULL)) {
        // Press Enter
        hid_event.ux_device_class_hid_event_buffer[keycode_offset] = 0x28;
        ux_device_class_hid_event_set(keyboard_instance, &hid_event);
        
        // Hold Enter key for 50 milliseconds
        HAL_Delay(50);

        // Release Enter key
        hid_event.ux_device_class_hid_event_buffer[keycode_offset] = 0;
        ux_device_class_hid_event_set(keyboard_instance, &hid_event);
      } else {
        // Report USB error by 10 fast blinks
        num_blinks = 10;
        blink_time_ms = 200;
      }

      // Blink LED high to low brightness
      const uint32_t num_blink_steps = 100;

      for (int blink = 0; blink < num_blinks; blink++) {
        for (uint32_t i = 0; i < num_blink_steps; i++) {
          uint32_t brightness =
              blink_high_brightness -
              (blink_high_brightness - blink_low_brightness) * i / num_blink_steps;

          __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, brightness);

          HAL_Delay(blink_time_ms / num_blink_steps);
        }
      }

      __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, inactive_brightness);
    }
  }
  /* USER CODE END 3 */
}

